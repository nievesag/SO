cat file # lee un archivo, saca el contenido del archivo
wc file  # saca el número de líneas, palabras y bytes de un archivo
head file # saca las 10 primeras líneas de archivo
tail +numero file # saca las últimas líneas del archivo empezando en numero
tr 'COSA1' 'COSA2' # sustituye letras y palabras, cosa1 por cosa2
sed [COMMAND] file # se usa para sustituir texto en un archivo
sort file # ordena un archivo alfabéticamente
cd directorio # cambiar de directorio de trabajo
ls # saca el directorio de trabajo actual
mkdir dirname # crea un directorio
rmdir # elimina directorios vacios
cp # copia archivos y directorios
find # busca archivos en la jerarquía de directorios
